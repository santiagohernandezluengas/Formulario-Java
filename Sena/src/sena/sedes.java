    //METODO DE CREAR
    void crear() {
        int id = Integer.parseInt(jTextField_id_sede.getText());
        String nom = jTextField_ingreso_sede.getText();
        String dir = jTextField_dir_sede.getText();

        try {
            String sql = "INSERT INTO sede VALUE('" + id + "' , '" + nom + "' , '" + dir + "')";

            cnn = cone.getConnection();
            st = cnn.createStatement();
            st.executeUpdate(sql);
            System.out.println("Sede creada");
            limpiarTabla();
        } catch (Exception e) {
        }

    }

    void limpiarTabla() {
        for (int i = 0; i <= jTable_sede.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }
    //METODO CREAR FIN

    //METODO CONSULTAR
    void consultar() {
        String sql = "select * from sede";

        try {
            cnn = cone.getConnection();
            st = cnn.createStatement();
            rs = st.executeQuery(sql);

            Object[] sede = new Object[3];
            model = (DefaultTableModel) jTable_sede.getModel();

            while (rs.next()) {
                sede[0] = rs.getInt("ID_sede");
                sede[1] = rs.getString("Nombre_sede");
                sede[2] = rs.getString("Direccion_sede");

                model.addRow(sede);
            }
            jTable_sede.setModel(model);
            System.out.println("Sede consultada");
        } catch (Exception e) {
        }
    }
